package com.bridge.addressbook;


public class Contacts {
    private String fName, lName, address, city, state, email;
    private long phoneNum;
    private int zipCode;

    public Contacts() {
    }

    public Contacts(String ajay, String nagar, String s, String s1, String karnataka, String s2, int i, int i1) {
    }


    void Contacts(String fName, String lName, String address, String city, String state, String email, long phoneNum, int zipCode) {
        this.fName = fName;
        this.lName = lName;
        this.address = address;
        this.city = city;
        this.state = state;
        this.email = email;
        this.phoneNum = phoneNum;
        this.zipCode = zipCode;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public String getlName() {
        return lName;
    }

    public void setlName(String lName) {
        this.lName = lName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(long phoneNum) {
        this.phoneNum = phoneNum;
    }

    public int getZipCode() {
        return zipCode;
    }

    public void setZipCode(int zipCode) {
        this.zipCode = zipCode;
    }
}